int	ft_iterative_factorial(int nb)
{
	int	ret;
	int	i;

	ret = 1;
	i = 1;
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		ret *= i;
		i++;
	}
	return (ret);
}
